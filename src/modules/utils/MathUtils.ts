/**
 * Calcula la desviación estándar de un conjunto de valores
 */
export function calculateStandardDeviation(values: number[]): number {
  return Math.sqrt(calculateVariance(values));
}

/**
 * Calcular el componente AC (amplitud) de una señal
 */
export function calculateAC(values: number[]): number {
  if (values.length === 0) return 0;
  return Math.max(...values) - Math.min(...values);
}

/**
 * Calcular el componente DC (valor promedio) de una señal
 */
export function calculateDC(values: number[]): number {
  if (values.length === 0) return 0;
  return values.reduce((sum, val) => sum + val, 0) / values.length;
}

/**
 * Calcula el índice de perfusión (PI) de una señal PPG
 * PI = AC/DC
 */
export function calculatePerfusionIndex(values: number[]): number {
  const ac = calculateAC(values);
  const dc = calculateDC(values);
  return dc !== 0 ? ac / dc : 0;
}

/**
 * Normalizar un valor al rango [0, 1]
 */
export function normalizeValue(value: number, min: number, max: number): number {
  if (max === min) return 0.5;
  return (value - min) / (max - min);
}

/**
 * Calcula la media móvil exponencial (EMA)
 */
export function calculateEMA(newValue: number, prevEMA: number | null, alpha: number = 0.2): number {
  if (prevEMA === null) return newValue;
  return alpha * newValue + (1 - alpha) * prevEMA;
}

/**
 * Filtrar outliers usando el método IQR (Rango Intercuartil)
 */
export function filterOutliers(values: number[]): number[] {
  if (values.length < 4) return [...values];
  
  const sorted = [...values].sort((a, b) => a - b);
  const q1Index = Math.floor(sorted.length * 0.25);
  const q3Index = Math.floor(sorted.length * 0.75);
  
  const q1 = sorted[q1Index];
  const q3 = sorted[q3Index];
  const iqr = q3 - q1;
  
  const lowerBound = q1 - 1.5 * iqr;
  const upperBound = q3 + 1.5 * iqr;
  
  return values.filter(v => v >= lowerBound && v <= upperBound);
}

